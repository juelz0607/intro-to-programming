# Write a program that uses a has to store a list of movie titles with the year 
# they came out. Then use the "puts" command to make your program print out the 
# year of each movie to the screen.

# Declaring Hashes

movies = { titanic: 1997, moneyball: 2011, belly: 1998 }

# Writing Expressions

puts movies[:titanic]
puts movies[:moneyball]
puts movies[:belly]